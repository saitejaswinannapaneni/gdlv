	<h3>Keeping Worry in Perspective</h3>
			While gestational diabetes is a cause for concern, the good news is that you and your health care team — your doctor, obstetrician, nurse educator, and dietitian — work together to lower your high blood glucose levels. And with this help, you can turn your concern into a healthy pregnancy for you, and a healthy start for your baby.<hr>

	<h3>Gestational Diabetes – Looking Ahead</h3>
			Gestational diabetes usually goes away after pregnancy. But once you've had gestational diabetes, your chances are 2 in 3 that it will return in future pregnancies. In a few women, however, pregnancy uncovers type 1 or type 2 diabetes. It is hard to tell whether these women have gestational diabetes or have just started showing their diabetes during pregnancy. These women will need to continue diabetes treatment after pregnancy.

			Many women who have gestational diabetes go on to develop type 2 diabetes years later. There seems to be a link between the tendency to have gestational diabetes and type 2 diabetes. Gestational diabetes and type 2 diabetes both involve insulin resistance. Certain basic lifestyle changes may help prevent diabetes after gestational diabetes.<hr>

	<h3>	Lower Your Risk for Diabetes by Losing Weight</h3>
			Are you more than 20% over your ideal body weight? Losing even a few pounds can help you avoid developing type 2 diabetes.<br>
			Making healthy food choices Follow simple daily guidelines, like eating a variety of foods including fresh fruits and vegetables, limiting fat intake to 30% or less of daily calories, and watching your portion size. Healthy eating habits can go a long way in preventing diabetes and other health problems.<br>

			Exercising Regular exercise allows your body to use glucose without extra insulin. This helps combat insulin resistance and is what makes exercise helpful to people with diabetes. Never start an exercise program without checking with your doctor first.<br>
