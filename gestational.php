			<h3><b>How Gestational Diabetes Can Affect Your Baby ?</b></h3><br>
        <div class="col-md-13">
        	Gestational diabetes affects the mother in late pregnancy, after the baby's body has been formed, but while the baby is busy growing. Because of this, gestational diabetes does not cause the kinds of birth defects sometimes seen in babies whose mothers had diabetes before pregnancy.<br><hr>

			However, untreated or poorly controlled gestational diabetes can hurt your baby. When you have gestational diabetes, your pancreas works overtime to produce insulin, but the insulin does not lower your blood glucose levels. Although insulin does not cross the placenta, glucose and other nutrients do. So extra blood glucose goes through the placenta, giving the baby high blood glucose levels. This causes the baby's pancreas to make extra insulin to get rid of the blood glucose. Since the baby is getting more energy than it needs to grow and develop, the extra energy is stored as fat.<br><hr>

			This can lead to macrosomia, or a "fat" baby. Babies with macrosomia face health problems of their own, including damage to their shoulders during birth. Because of the extra insulin made by the baby's pancreas, newborns may have very low blood glucose levels at birth and are also at higher risk for breathing problems. Babies with excess insulin become children who are at risk for obesity and adults who are at risk for type 2 diabetes.<br><hr>
