        <div class="col-md-13">
        	Pregnant women who have never had diabetes before but who have high blood glucose (sugar) levels during pregnancy are said to have gestational diabetes. According to a 2014 analysis by the Centers for Disease Control and Prevention, the prevalence of gestational diabetes is as high as 9.2%.<br><hr>

			We don't know what causes gestational diabetes, but we have some clues. The placenta supports the baby as it grows. Hormones from the placenta help the baby develop. But these hormones also block the action of the mother's insulin in her body. This problem is called insulin resistance. Insulin resistance makes it hard for the mother's body to use insulin. She may need up to three times as much insulin.<br><hr>

			Gestational diabetes starts when your body is not able to make and use all the insulin it needs for pregnancy. Without enough insulin, glucose cannot leave the blood and be changed to energy. Glucose builds up in the blood to high levels. This is called hyperglycemia. You may also be interested in our book.
