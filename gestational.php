			<h3><b>How to Treat  Gestational diabetes?  </b></h3><br>
			<div class="col-md-5">
			<img src="./images/gestational3.png" alt="risk" id="image" class="img-responsive" style="display: inline-block;"><hr>
			</div>
        <div class="col-md-13">
        	Because gestational diabetes can hurt you and your baby, you need to start treatment quickly.<br>

			Treatment for gestational diabetes aims to keep blood glucose levels equal to those of pregnant women who don't have gestational diabetes. Treatment for gestational diabetes always includes special meal plans and scheduled physical activity. It may also include daily blood glucose testing and insulin injections.<br>

			If you're testing your blood glucose, the American Diabetes Association suggests the following targets for women who develop gestational diabetes during pregnancy. More or less stringent glycemic goals may be appropriate for each individual.<br>

				Before a meal (preprandial): 95 mg/dl or less<br>
				1-hour after a meal (postprandial): 140 mg/dl or less<br>
				2-hours after a meal (postprandial): 120 mg/dl or less<br>
			You will need help from your doctor, nurse educator, and other members of your health care team so that your treatment for gestational diabetes can be changed as needed. For you as the mother-to-be, treatment for gestational diabetes helps lower the risk of a cesarean section birth that very large babies may require.<br>

			Sticking with your treatment for gestational diabetes will give you a healthy pregnancy and birth, and may help your baby avoid future poor health.<br>
