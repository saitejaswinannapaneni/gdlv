				<div class="col-md-6">
			
				Nutrition is one of the most important pieces of the diabetes puzzle. Learning how different foods affect your blood glucose and how manage that within your daily routine will be key.  Working with a registered dietitian who has expertise in type 1 diabetes to establish a personalized eating plan is a great place to start. This will include a strategy for balancing food, insulin doses, and physical activity. It will be important to revisit your eating plan as your preferences and daily activities change over time.  A dietitian can also provide valuable guidance if you need to lose weight.<hr>
