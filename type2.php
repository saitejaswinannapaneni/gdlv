				 <div class="col-md-13"><h3>How Do I Check?</h3>
				People with diabetes check their blood glucose levels by poking their fingertips and using a blood glucose meter, or use a continuous glucose monitor that tells what their blood glucose is at in that moment. The A1C is also a blood test but it tells us what blood glucose levels have been over the last two to three months.<hr>

				<h3>How to check with a meter:</h3>
				        After washing your hands, insert a test strip into your meter.
						Use your lancing device on the side of your fingertip to get a drop of blood.
						Touch and hold the edge of the test strip to the drop of blood, and wait for the result.
						Your blood glucose level will appear on the meter's display.
					    Note: All meters are slightly different, so always refer to your user's manual for specific instructions.<hr>
					</div>

				 <div class="col-md-13"><h3>Other tips for checking:</h3>

						With some meters, you can also use your forearm, thigh or fleshy part of your hand.
						There are spring-loaded lancing devices that make sticking yourself less painful.
						If you use your fingertip, stick the side of your fingertip by your fingernail to avoid having sore spots on the frequently used part of your finger.<br>
